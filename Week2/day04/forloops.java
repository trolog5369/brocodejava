
public class forloops {
    public static void main(String[] args) {
        // For loop= Execute some code a CERTAIN amount of times
        // similar to a while loop 
        // While loop= could execute  INFINITE amount of time until condition is no longer true(false)
        // for loop=We executes/wanna run it certain amount of time!

        // for(initilization(counter);condition;step(increment/decrement)){
        //    counter is also known as loop control variable!
        // }

        for (int i = 0; i <10 ; i++) {
            System.out.println(i);
        }

        for (int i = 10; i >0 ; i--) {
            System.out.println(i);
        }

        //We can aslo incerment by a certain number
        for (int i = 1; i <=10 ; i+=2) {
            System.out.println(i);
        }
        // For loop is good if we wanna run a code a certain amount of time!
        // But theres a little bit of setup we have to do we will need some sort of index or a counter
        // a condition in which we have to stop and ipdate the counter as per necessary

        //INITIALIZE:CONDITION:STEP
    }
}
