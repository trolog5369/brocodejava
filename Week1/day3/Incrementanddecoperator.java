
public class Incrementanddecoperator {
    public static void main(String[] args) {
        // Increment and Decrement operators

        int x=1;

        // Increment operator:
        // x=x+1; 
        // or x+=1 or
        x++;
        
        // Decrement operator:
        // x=x-1;
        // or x-=1 or
        x--;

        System.out.println(x);
    }
}
